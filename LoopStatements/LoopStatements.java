package com.LoopStatements;

public class LoopStatements {
    public static void main(String[] args) {
        int i = 1;
        do {
        System.out.println(i); // exit control loop
            i++;
        }
            while (i < 10);   // increment operator is compulsory in while loop
    }                        // while loop executes until the condition is true and if executes only once
}
                             // do is exit control loop and while is entry controlled loop

